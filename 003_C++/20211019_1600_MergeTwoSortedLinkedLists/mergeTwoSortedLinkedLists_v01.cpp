SinglyLinkedListNode* mergeLists(SinglyLinkedListNode* head1, SinglyLinkedListNode* head2) {
    SinglyLinkedListNode *head, *p1, *p2, *p;
    p1 = head1;
    p2 = head2;
    p = nullptr;
    // case some headx are null --> return the other head.
    if(head1 == nullptr){
        return head2;
    }
    if(head2 == nullptr){
        return head1;
    }
    // choose 1st element, initialize head pointer
    if(head1->data <= head2->data){
        head = head1;
        p1 = head1->next;
        p2 = head2;
    }
    else{
        head = head2;
        p2 = head2->next;
        p1 = head1;
    }
    p = head;
    
    while((p1 != nullptr) && (p2 != nullptr)){
        // merge between 2 lists
        if(p1->data <= p2->data){
            p->next = p1;
            p = p1;
            p1 = p1->next;
        }
        else{
            p->next = p2;
            p = p2;
            p2 = p2->next;
        }
    }
    // if one of 2 pointeurs are null (p1 or p2) link final list to the other pointer
    if(p1 == nullptr){
        p->next = p2;
    }
    else{
        p->next = p1;
    }
    return head;
}

